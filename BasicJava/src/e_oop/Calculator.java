package e_oop;

public class Calculator {

	// 5개의 메서드, 2개의 파라미터

	long plus(long a, long b) {
		return a + b;
	}

	long minus(long a, long b) {
		return a - b;
	}

	long multiple(long a, long b) {
		return a * b;
	}

	long divide(long a, long b) {
		return a / b;
	}

	long remainder(long a, long b) {
		return a % b;
	}

}
